/*函数名：初始化WiFi的复位IO                       */
/*参  数：无                                       */
/*返回值：无                                       */
/*-------------------------------------------------*/
void WiFi_ResetIO_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;                      //定义一个设置IO端口参数的结构体
	RCC_APB2PeriphClockCmd( RCC_APB2Periph_GPIOB , ENABLE);   //使能PA端口时钟
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_7;                 //准备设置PB7
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;         //速率50Mhz
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;   		  //推免输出方式
	GPIO_Init(GPIOB, &GPIO_InitStructure);            		  //设置PA4
	RESET_IO(1);                                              //复位IO拉高电平
}
/*-------------------------------------------------*/
/*函数名：WiFi发送设置指令                         */
/*参  数：cmd：指令                                */
/*参  数：timeout：超时时间（100ms的倍数）         */
/*返回值：0：正确   其他：错误                     */
/*-------------------------------------------------*/
char WiFi_SendCmd(char *cmd, int timeout)
{
	WiFi_RxCounter=0;                           //WiFi接收数据量变量清零                        
	memset(WiFi_RX_BUF,0,WiFi_RXBUFF_SIZE);     //清空WiFi接收缓冲区 
	Serial_Printf("\r\n cmd:%s\r\n",cmd);
	//Serial3_SendString(cmd);
	
	WiFi_printf("%s\r\n",cmd);                  //发送指令
	//Serial_Printf("\r\n 指令发送成功");
	while(timeout--){                           //等待超时时间到0
		delay_ms(100);                          //延时100ms
		if(!strstr(WiFi_RX_BUF,"AT\r\nOK\r\n"))            //如果接收到OK表示指令成功
			break;       						//主动跳出while循环
			
		Serial_Printf("\r\n");
		Serial_Printf("%d ",timeout);               //串口输出现在的超时时间
	}
	Serial_Printf("\r\n WiFi_RX_BUF:%s",WiFi_RX_BUF);
	Serial_Printf("\r\n");                          //串口输出信息
	if(timeout<=0)return 1;                     //如果timeout<=0，说明超时时间到了，也没能收到OK，返回1
	else return 0;		         				//反之，表示正确，说明收到OK，通过break主动跳出while
}
